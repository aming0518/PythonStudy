print("hello123我".isalnum())#判断字符串是否由数字字母组成，汉字当做字母
print("13213213213".isdigit())#判断字符串是否纯数字
print("dadad我".isalpha())#判断字符串是否纯字母
print("abc我".islower())#判断是否为小写
print("abc我".isupper())#判断是否为大写
print(" ".isspace())#判断字符串是否纯空格组成
print("hello".istitle())#判断字符串是否首字母大写且其他字母小写
print("0x109".isdecimal())#判断进制
